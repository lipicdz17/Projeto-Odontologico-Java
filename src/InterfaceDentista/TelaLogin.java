    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        
       try{
            con.executaSql("select * from bdfuncionario where nome= '"+usuario.getText()+"'");
            con.rs.first();
            if(con.rs.getString("senha").equals(senha.getText())){
            TelaInicial inicial = new TelaInicial(usuario.getText());
            inicial.setVisible(true);
            dispose();
           
        }else{
        JOptionPane.showMessageDialog(rootPane,"Usuario ou Senha Invalida!!\n Tente novamente!!");
            }       
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane,"Usuario ou Senha Invalida!!\n Tente novamente!!");
        }
    }//GEN-LAST:event_entrarActionPerformed
     
